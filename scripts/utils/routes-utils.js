function getLevenshteinDistance(a, b) {
    const matrix = Array.from({ length: a.length + 1 }, () =>
        Array(b.length + 1).fill(0)
    );

    for (let i = 0; i <= a.length; i++) matrix[i][0] = i;
    for (let j = 0; j <= b.length; j++) matrix[0][j] = j;

    for (let i = 1; i <= a.length; i++) {
        for (let j = 1; j <= b.length; j++) {
            if (a[i - 1] === b[j - 1]) {
                matrix[i][j] = matrix[i - 1][j - 1];
            } else {
                matrix[i][j] = 1 + Math.min(
                    matrix[i - 1][j],    // deletion
                    matrix[i][j - 1],    // insertion
                    matrix[i - 1][j - 1] // substitution
                );
            }
        }
    }

    return matrix[a.length][b.length];
}



export  function searchMatchingArea(address) {
    const lowerAddress = address.toLowerCase();
    let closest = { suburb: null, zone: null, distance: Infinity };

    for (let i = 0; i < zoneSuburbs.length; i++) {
        const suburbList = zoneSuburbs[i];
        for (let area of suburbList) {
            const areaLower = area.toLowerCase();

            // Exact match or substring match
            if (lowerAddress.includes(areaLower)) {
                return { match: true, suburb: area, zone: zoneNames[i] };
            }

            // Compute distance for closest suggestion
            const distance = getLevenshteinDistance(lowerAddress, areaLower);
            if (distance < closest.distance) {
                closest = { suburb: area, zone: zoneNames[i], distance };
            }
        }
    }

    return { match: false, closest };
}